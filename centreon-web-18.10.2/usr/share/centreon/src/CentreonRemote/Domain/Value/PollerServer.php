<?php

namespace CentreonRemote\Domain\Value;

class PollerServer
{

    private $id;

    private $ip;


    public function getId()
    {
        return $this->id;
    }

    public function setId($id)
    {
        $this->id = $id;
    }

    public function getIp()
    {
        return $this->ip;
    }

    public function setIp($ip)
    {
        $this->ip = $ip;
    }
}
